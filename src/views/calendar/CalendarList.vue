<template>
  <div>
    <div class="calendar-wrap">
      <div class="month-wrap">
        <button class="btn-prev-month" @click="renderPrevMonth">&lt;</button>
        <p class="curr-month">{{ currentYear }}년 {{ currentMonth + 1 }}월</p>
        <button class="btn-next-month" @click="renderNextMonth">&gt;</button>
      </div>
      <div class="calendar">
        <div class="cal-header">
          <div class="day">월</div>
          <div class="day">화</div>
          <div class="day">수</div>
          <div class="day">목</div>
          <div class="day">금</div>
          <div class="day">토</div>
          <div class="day">일</div>
        </div>
        <div class="cal-content">
          <div v-for="(item, index) in calendarData" :key="index"
                class="date"
                :class="{
                  disable: item.disable,
                  today: currentYear === today.year && currentMonth === today.month && item.date === today.date }"
                @click="openListModal(item)">
            <span>{{ item.date }}</span>
            <em v-if="item?.data && item?.data?.length > 0" class="mark">💜</em>
          </div>
        </div>
      </div>
    </div>

<!--    <button class="btn-write-cal" @click="writeCal">-->
<!--      <i class="xi-pen"></i>-->
<!--    </button>-->

    <div v-if="this.modal.listModalOpened">
      <div class="modal" :class="{opened: this.modal.listModalOpened}">
        <div class="modal-top">
          <p class="date">{{ this.modal.date }}일 {{ this.modal.day }}요일</p>
          <button>
            <i class="xi-close" @click="closeListModal"></i>
          </button>
        </div>
        <div class="modal-content">
          <p class="m-count">총 {{ this.modal.scheduleList.length }}개</p>
          <ul>
            <li v-for="(schedule, index) in this.modal.scheduleList" :key="index">
              <p>{{ schedule.contents }}</p>
            </li>
            <li class="m-content" @click="openViewModal">
              <p><i class="xi-plus"></i> 일정 추가하기</p>
            </li>
          </ul>
        </div>
      </div>
      <div class="dimmed"></div>
    </div>

    <div v-if="this.modal.viewModalOpened">
      <div class="modal" :class="{opened: this.modal.viewModalOpened}">
        <div class="modal-top">
          <p class="date">{{ this.modal.date }}일 {{ this.modal.day }}요일</p>
          <button>
            <i class="xi-close" @click="closeViewModal"></i>
          </button>
        </div>
        <div class="modal-content">
          <textarea class="content-wrap"></textarea>
          <div class="btn-wrap">
            <button class="btn-delete">
              <i class="xi-trash"></i>
            </button>
            <button class="btn-save">저장</button>
          </div>
        </div>
      </div>
      <div class="dimmed"></div>
    </div>

  </div>
</template>

<script>
// import ModalView from '@/components/modalView';

export default {
  name: "CalendarList",

  computed: {
    calendarList () {
      return this.$store.getters["moduleCalendar/calendarList"];
    },
  },

  components: {
    // ModalView,
  },

  data() {
    return {
      today: {
        year: '',
        month: '',
        date: '',
        day: ''
      },

      lastMonthLastDate: 0,
      lastMonthLastDay: 0,
      thisMonthLastDate: 0,
      thisMonthLastDay: 0,

      currentYear: 0,
      currentMonth: 0,
      currentDate: 0,

      calendarData: [],

      modal: {
        // opened: false,
        listModalOpened: false,
        viewModalOpened: false,
        date: '',
        day: '',
        scheduleList: [],
      }

    }
  },

  methods: {
    renderCalendar(thisMonthDate)  {
      // month 는 0이 1월, 1이 2월 ... 11이 12월
      // day는 0이 일요일, 1이 월요일 ... 6이 토요일

      this.currentYear = thisMonthDate.getFullYear();
      this.currentMonth = thisMonthDate.getMonth();
      this.currentDate = thisMonthDate.getDate();


      // Date 객체를 만들 때 날짜를 0으로 지정하면 지난 달의 마지막 날짜를 가진 Date 객체가 반환된다.

      // 지난 달의 마지막 날의 날짜와 요일
      const lastMonthLastD = new Date(this.currentYear, this.currentMonth, 0);
      this.lastMonthLastDate = lastMonthLastD.getDate();
      this.lastMonthLastDay = lastMonthLastD.getDay();

      // 이번 달의 마지막 날의 날짜와 요일
      const thisMonthLastD = new Date(this.currentYear, this.currentMonth + 1, 0);
      this.thisMonthLastDate = thisMonthLastD.getDate();
      this.thisMonthLastDay = thisMonthLastD.getDay();


      let calendarData = [];

      // 지난달 렌더링
      for (let i = this.lastMonthLastDate - this.lastMonthLastDay + 1; i <= this.lastMonthLastDate; i++) {
        calendarData.push({
          date : i,
          disable: true,
        });
      }

      // 이번달 렌더링
      for (let i = 1; i < this.thisMonthLastDate + 1; i++) {
        calendarData.push({
          date : i,
          disable: false,
          data: [],
        });

        // 이번달의 날짜에 해당하는 일정을 calendarDate.data 배열에 push
        this.$store.getters["moduleCalendar/calendarList"].filter((v) => {
          if (i ===  new Date(v.regDate).getDate()) {
            calendarData[i+1].data.push(v);
          }
        });
      }

      // 다음달 렌더링
      for (let i = 1; i <= 7 - this.thisMonthLastDay; i++) {
        calendarData.push({
          date : i,
          disable: true,
        });
      }

      this.calendarData = calendarData;

    },

    fetchCalendar() {
      const userNo = this.$store.getters["moduleUser/getSignedInUserData"].userNo;
      this.$store.dispatch('moduleCalendar/CALENDAR_LIST', {
        userNo,
        year: this.currentYear,
        month: this.currentMonth + 1,
      })
          .then(response => {
            console.log(response);
            this.renderCalendar( new Date( this.currentYear, this.currentMonth, this.currentDate ) );
          })
          .catch(e => {
            console.log(e);
          });
    },

    renderPrevMonth() {
      // this.renderCalendar(new Date(this.currentYear, this.currentMonth - 1, 1));

      this.currentMonth = this.currentMonth - 1;
      this.fetchCalendar();
    },

    renderNextMonth() {
      // this.renderCalendar(new Date(this.currentYear, this.currentMonth + 1, 1));

      this.currentMonth = this.currentMonth + 1;
      this.fetchCalendar();
    },

    openListModal(item) {
      this.modal.listModalOpened = true;
      this.modal.scheduleList = item.data;
      this.modal.date = item.date;
      let day = new Date(this.currentYear, this.currentMonth, item.date).getDay();
      switch (day) {
        case 1:
          day = '월';
          break;
        case 2:
          day = '화';
          break;
        case 3:
          day = '수';
          break;
        case 4:
          day = '목';
          break;
        case 5:
          day = '금';
          break;
        case 6:
          day = '토';
          break;
        case 0:
          day = '일';
          break;
      }
      this.modal.day = day;
    },

    closeListModal() {
      this.modal.listModalOpened = false;
    },

    openViewModal() {
      this.modal.viewModalOpened = true;
    },

    closeViewModal() {
      this.modal.viewModalOpened = false;
    },

  },

  mounted() {
    // 참고 블로그: https://songsong.dev/11
    // API: 121.161.237.50:50005/swagger-ui/index.html

    const today = new Date();
    this.today.year = today.getFullYear();
    this.today.month = today.getMonth();
    this.today.date = today.getDate();
    this.today.day = today.getDay();

    // 달력에서 표기하는 년, 월, 일
    this.currentYear = today.getFullYear();
    this.currentMonth = today.getMonth();
    this.currentDate = today.getDate();

    // this.renderCalendar( new Date( this.currentYear, this.currentMonth, this.currentDate ) );
    this.fetchCalendar();

  },
}
</script>

